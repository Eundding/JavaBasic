package chap_01;

public class _02_DataTypes {
    // psvm 치면 메인 메소드 자동 생성
    // main 쳐도 메인 메소드 자동 생성
    public static void main(String[] args) {
        // sout 치면 System.out.println(); 자동 생성
        System.out.println("Hello World"); // 문자열
        System.out.println(12); // Int
        System.out.println(-34);
        System.out.println(3.14);
        System.out.println(true); // Bool
        System.out.println(false);
        System.out.println(); // 빈 줄
        System.out.println(123+345); // 계산
    }
}
